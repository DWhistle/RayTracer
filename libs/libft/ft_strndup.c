#include "libft.h"

char	*ft_strndup(const char *s1, size_t len)
{
	char	*ret;
	char	*begin;

	ret = ft_strnew(len);
	if (!ret)
		return (NULL);
	begin = ret;
	while (*s1 && len--)
		*ret++ = *s1++;
	*ret = 0;
	return (begin);
}
